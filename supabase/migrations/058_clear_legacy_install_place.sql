-- 설치장소(units.install_place)에 잘못 들어간 "호기 라벨" 잔재 정리 (2026-07-24)
--
-- 배경: install_place는 "합참본부-1", "별관-3", "교회" 같은 실제 설치장소를 담는 칸인데,
-- 초기 임포트 때 레거시 호기 라벨(1-{seq})이 그대로 들어가 876대 중 782대가 잔재였다.
-- 그 결과 설치장소가 사실상 쓸 수 없는 데이터가 되어 화면 어디에서도 쓰이지 않고 있었다.
--
-- 정리 규칙: install_place가 '1-숫자' 형태인 것만 NULL로 비운다.
--   - 778대: 정확히 '1-{seq}' (호기 라벨 잔재 확정)
--   - 4대  : seq와 안 맞는 '1-1'/'1-2' (금화프라자·뉴베리청담·상진빌리지 — 상진빌리지는 1↔2가 뒤바뀜)
--
-- 남기는 것(실제 동 번호로 판단, 8대):
--   동보수목원아파트 101-1/101-2 · 솔레움오피스텔 101-1/102-1 · 한신20차APT 339-1~339-4
--   → '1-숫자' 패턴이 아니라 이 UPDATE에 걸리지 않는다.
--
-- 앱에서도 lib/utils.js realInstallPlace()가 '1-숫자'를 설치장소로 취급하지 않는다(이중 방어).

update units
set install_place = null
where install_place ~ '^1-[0-9]+$';
