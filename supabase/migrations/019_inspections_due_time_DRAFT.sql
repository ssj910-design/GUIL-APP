-- 019. [초안] inspections에 검사일정 시간(due_time) 컬럼 추가
--
-- 배경: 관리자가 수기입력하는 검사예정일(inspections.due_date)은 날짜만
-- 있었는데, 조건부/불합격 현장을 언제 방문해서 조치할지 시간까지 잡아서
-- 관리자 대시보드·기사앱 홈 조건부/불합격 카드에 함께 보여달라는 요청.
-- due_date(날짜)는 그대로 두고 시간만 별도 컬럼에 저장한다
-- (기존 날짜 비교·정렬 로직에 영향 없음).
--
-- 실행: Supabase 대시보드 SQL Editor에서 아래 구문을 직접 실행할 것.
-- ============================================================
alter table public.inspections add column if not exists due_time text;
